package Pojo;

public class equipo {
    String NomEqui;
    String NomCap;
    String Tel;
    String Hora;
    String Fecha;
    String Pago;

    public String getNomEqui() {
        return NomEqui;
    }

    public void setNomEqui(String nomEqui) {
        NomEqui = nomEqui;
    }

    public String getNomCap() {
        return NomCap;
    }

    public void setNomCap(String nomCap) {
        NomCap = nomCap;
    }

    public String getTel() {
        return Tel;
    }

    public void setTel(String tel) {
        Tel = tel;
    }

    public String getHora() {
        return Hora;
    }

    public void setHora(String hora) {
        Hora = hora;
    }

    public String getFecha() {
        return Fecha;
    }

    public void setFecha(String fecha) {
        Fecha = fecha;
    }

    public String getPago() {
        return Pago;
    }

    public void setPago(String pago) {
        Pago = pago;
    }
}
